//7. Use increment and decrement operators and observe the result.

public class Main {
    public static void main(String[] args) {
        int n = 10;
        int sucessor = ++n;
        System.out.println("The sucessor is : " + sucessor);

        n = 10;
        int antecessor = --n;
        System.out.println("The antecessor is: " + antecessor);
    }
}