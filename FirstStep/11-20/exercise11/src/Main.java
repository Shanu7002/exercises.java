//11. Check if a number is positive, negative, or zero.

public class Main {
    public static void main(String[] args) {
        int n = 0;
        if(n > 0){
            System.out.print("The number is positive");
        } else if (n < 0){
            System.out.print("The number is negative");
        } else {
            System.out.print("The number is zero");
        }

    }
}
